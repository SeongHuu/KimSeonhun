// 데이터 타입
package Part2_variable;

public class PrimitiveDataTypeExample {

	public static void main(String[] args) {

//	    boolean형 데이터 타입
		boolean isFun = true;
		System.out.println(isFun);
		
//		문자타입,캐릭터타입
		char c='f';
		
//		정수타입
		int x=59;
		
		long bing=345345l;

//		실수타입
		float f=32.4f;
		
		double d=345345.6;
	}

}
